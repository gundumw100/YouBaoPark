package com.yirui.park.model;

public class Stat {

	private String shop_id;
	private String year;
	private String month;
	private String grant_size;
	private String buy_size;
	private String total;

	public String getShop_id() {
		return shop_id;
	}

	public void setShop_id(String shop_id) {
		this.shop_id = shop_id;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getGrant_size() {
		return grant_size;
	}

	public void setGrant_size(String grant_size) {
		this.grant_size = grant_size;
	}

	public String getBuy_size() {
		return buy_size;
	}

	public void setBuy_size(String buy_size) {
		this.buy_size = buy_size;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

}
